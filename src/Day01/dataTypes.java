package Day01;

public class dataTypes {
    public static void main(String[] args) {
        // Int data types
        int x = 5;
        int y = 12;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println("Value of x = "+ (x++));
        System.out.println(--y);
        System.out.println("Value of x = "+ x);

        char ch = 'a';
        System.out.println(ch);

        double d = 3.1415;
        System.out.println(d+x);

    }
}
